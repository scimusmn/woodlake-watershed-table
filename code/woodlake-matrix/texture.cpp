int mod(int a, int b) {
  int ret = a % b;
  if (ret < 0) {
    ret += b;
  }
  return ret;
}


uint8_t sample(texture_t tex, int x, int y) {
  int xm = mod(x, tex.w);
  int ym = mod(y, tex.h);
  return tex.data[ym*tex.w + xm];
}


